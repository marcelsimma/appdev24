using System;

namespace FatmanurKeles.Week02
{
    public class CountTeilen
    {
        public static void MainTeilen(string[] args)
        {

            for (float i = 0; i <= 20; i++)
            {
                if (i <= 20)
                {
                    Console.WriteLine(i);
                    Console.WriteLine(i + " / 5 = " + i / 5);
                    Console.WriteLine(i + " / 5.0 = " + i / 5.0);
                }
            }

        }
    }
}