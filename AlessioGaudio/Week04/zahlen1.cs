    static string replace(string input, int startzahl, int entzahl)
    {
        string result = "";
        foreach (char c in input)
        {
            if (c < startzahl || c > entzahl)
            {
                result += c;
            }
        }
        return result;
    }

    static string replace(string input, int startzahl1, int endzahl1, int startzahl2, int endzahl2)
    {
        string result = "";
        foreach (char c in input)
        {
            if ((c < startzahl1 || c > startzahl1) && (c < startzahl2 || c > endzahl2))
            {
                result += c;
            }
        }
        return result; 
